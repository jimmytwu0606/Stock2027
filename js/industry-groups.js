// ─────────────────────────────────────────────────────
// 反向索引:股票代號 → 它屬於哪些族群
// ─────────────────────────────────────────────────────
// 用 lazy build:第一次呼叫時才建立,後續直接讀
let _stockToIndustries = null;

function _buildReverseIndex() {
  if (_stockToIndustries) return _stockToIndustries;
  _stockToIndustries = {};
  for (const [industry, stocks] of Object.entries(INDUSTRY_GROUPS)) {
    for (const code of stocks) {
      if (!_stockToIndustries[code]) _stockToIndustries[code] = [];
      _stockToIndustries[code].push(industry);
    }
  }
  return _stockToIndustries;
}

/**
 * 查詢某支股票屬於哪些族群
 * @param {string} code 4 位數股票代號
 * @returns {string[]}  族群名稱陣列(可能多個,沒分類則為 [])
 *
 * @example
 *   getIndustriesOf('2330')  // ['半導體製造', 'AI ASIC 概念']
 *   getIndustriesOf('9999')  // []
 */
export function getIndustriesOf(code) {
  const map = _buildReverseIndex();
  return map[code] ?? [];
}

/**
 * 查詢某族群有哪些股票
 * @param {string} industry 族群名稱(必須與 INDUSTRY_GROUPS 的 key 完全一致)
 * @returns {string[]}  股票代號陣列
 */
export function getStocksInIndustry(industry) {
  return INDUSTRY_GROUPS[industry] ?? [];
}

/**
 * 查詢「跟某支股票同族群」的其他股票(不含自己)
 * 若該股屬於多個族群,回傳所有族群成員的聯集
 * @param {string} code 4 位數股票代號
 * @returns {string[]}  其他同族群股票代號(去重後)
 *
 * @example
 *   getPeersOf('8021')  // PCB 族群成員,不含 8021 自己
 */
export function getPeersOf(code) {
  const industries = getIndustriesOf(code);
  if (!industries.length) return [];
  const set = new Set();
  for (const ind of industries) {
    for (const peer of getStocksInIndustry(ind)) {
      if (peer !== code) set.add(peer);
    }
  }
  return Array.from(set);
}

/**
 * 查詢所有族群名稱
 * @returns {string[]}
 */
export function getAllIndustries() {
  return Object.keys(INDUSTRY_GROUPS);
}
